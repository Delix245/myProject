# Репозиторий — хранилище файлов и истории изменений
# repository

# Пулл — забираем изменения из удалённого репозитория к себе
git pull

# Пуш — отправляем свои изменения на удалённый репозиторий (например, на GitHub)
git push

# Коммит — сохраняем изменения в локальной версии проекта
git commit -m "Описание изменений"

# Ветка — создаём новую ветку
git branch имя-ветки

# Переключение на другую ветку (чекаут)
git checkout имя-ветки

# Слияние веток (мердж) — объединяем текущую ветку с другой
git merge имя-ветки

# Пулл реквест — технически создаётся на сайтах типа GitHub, GitLab, Bitbucket
